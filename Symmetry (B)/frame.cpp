#include "frame.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \fn	void SymFrame::Init()
////////////////////////////////////////////////////////////////////////////////////////////////////

void SymFrame::Init()
{
	this->m_pTitleBar = new TitleBar();
	this->m_pTitleBar->SetActive(true);
	this->m_pTitleBar->SetParent(this);

	PrintDebugL("Init & children = %d", this->GetChildCount());
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \fn	void SymFrame::SetTitle(string sTitle)
////////////////////////////////////////////////////////////////////////////////////////////////////

void SymFrame::SetTitle(string sTitle)
{
	if (this->m_pTitleBar)
		this->m_pTitleBar->SetTitle(sTitle);
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \fn	string SymFrame::GetTitle()
////////////////////////////////////////////////////////////////////////////////////////////////////

string SymFrame::GetTitle()
{
	if (this->m_pTitleBar)
	{
		return this->m_pTitleBar->GetTitle();
	}
	else
		return "";
}

////////////////////////////////////////////////////////////////////////////////////////////////////
/// \fn	void SymFrame::Think()
////////////////////////////////////////////////////////////////////////////////////////////////////

void SymFrame::Think()
{
	if (this->m_pTitleBar)
	{
		int w, h;
		this->GetSize(&w, &h);

		this->m_pTitleBar->SetSize(w, 25);
	}
}
